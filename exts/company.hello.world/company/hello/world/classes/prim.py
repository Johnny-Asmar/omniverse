class Prim:
  def __init__(self, name, center_x, center_y, center_z, rot_x, rot_y, rot_z, width, height, depth):

    self.name = name

    self.center_x = center_x
    self.center_y = center_y
    self.center_z = center_z

    self.rot_x = rot_x
    self.rot_y = rot_y
    self.rot_z = rot_z

    self.width = width
    self.height = height
    self.depth = depth